package org.tcs.test;

public class Datatypes {
	
	public static void main(String[] args) {
		
		//datatype variablename = value;
		
		byte age = 127;
		System.out.println("Age value is:" +age);
		
		short atmpin = 1234;
		System.out.println("Atm pin is:" +atmpin);
		
		int pincode = 600096;
		System.out.println("Pin code is"+pincode);
		
		float weight = 69.50f;
		System.out.println("Weight is "+weight);
		
		long mobNo = 9841035448l;
		System.out.println("Mob no is" +mobNo);
		
		double salary = 3489.99;
		System.out.println("Salary is" +salary);
		
		char gender = 'M';
		System.out.println("Gender is"+gender);
		
		String name = "Raj";
		System.out.println("Name is"+name);
		
		boolean maritalstatus = true;
		System.out.println("Marital status is"+maritalstatus);
	}

}
